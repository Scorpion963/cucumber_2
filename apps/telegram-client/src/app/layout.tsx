export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const session = await auth.api.getSession({ headers: await headers() });
  if (!session?.user) return;
  console.log(session.user.email);

  const homeChats = await findHomeChatsForStore(session.user.id)

  const {mappedChatInfo, mappedContacts} = mapChatsToStore(homeChats);

  console.log("mapped: ", mappedChatInfo, mappedContacts)

  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased dark`}
      >
        <HomeChatsProvider chats={mappedChatInfo} contacts={mappedContacts}>
          <div className="w-full h-screen">
            <div className=" w-full h-full">
              <ResizablePanelGroup
                direction="horizontal"
                className="rounded-lg border w-full "
              >
                <ResizablePanel minSize={15} maxSize={30} defaultSize={20}>
                  <Sidebar />
                </ResizablePanel>
                <ResizableHandle />
                <ResizablePanel className="hidden lg:block" defaultSize={80}>
                  {children}
                </ResizablePanel>
              </ResizablePanelGroup>
            </div>
          </div>
          <Toaster position="top-center" />
        </HomeChatsProvider>
      </body>
    </html>
  );
}


